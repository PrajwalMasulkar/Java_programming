class ArithmeticOperator
{
    public static void main(String[] args)
    {
    double n1=12.5,n2=3.5,result;
    result = n1+n2;
    System.out.println(result);
    result = n1+n2;
    System.out.println(result);
    result = n1*n2;
    System.out.println(result);
    result = n1/n2;
    System.out.println(result);
    result = n1%n2;
    System.out.println(result);

     }
}